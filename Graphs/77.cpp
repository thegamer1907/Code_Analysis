#include<iostream>
#include<stdio.h>
#include<cstring>
#include<cmath>
using namespace std;
int main()
{
    int n,m;
    string s;
    cin>>n>>m;
    cin>>s;
    for(int i=1;i<=m;i++)
    for(int j=1;j<n;j++)
        if(s[j]=='G'&&s[j-1]=='B')
    {
        s[j]='B';
        s[j-1]='G';
        j++;
    }
    cout<<s<<endl;
    return 0;
}
