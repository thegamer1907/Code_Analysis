int dfs(int u,int par)
{
	int cnt=1;
	for(int i=0;i<a[u].size();i++)
	{
		int v=a[u][i];

		if(v!=par)
			cnt=max(cnt,1+dfs(v,u));
	}

	return cnt;
}


