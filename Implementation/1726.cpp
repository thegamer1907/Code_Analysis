#include<iostream>
#include<string.h>
#include<cmath>
using namespace std;
int main()
{
	int c=0,x=0;
	char ch[100];
	cin>>ch;
	for(int i=0;i<=strlen(ch);i++)
	{
		if(ch[i]%2==0)
		{c+=1;
		if(c>=7)
		{break;}}
		else
		{c=0;}
	}

	for(int i=0;i<=strlen(ch);i++)
	{
		if(ch[i]%2==1)
		{x+=1;
		if(x>=7)
		{break;}}
		else
		{x=0;}
	}
	if(c>=7||x>=7)
		cout<<"YES \n";
	else
		cout<<"NO \n";
}