using namespace std;

void solve()
{
	int n, k;
	cin >> n >> k;

	vector<int> arr(n);
	set<int> in;
	for (int i = 0; i < n; i++)
	{
		for (int j = 0; j < k; j++)
		{
			int bit;
			scanf("%d", &bit);
			arr[i] = arr[i] * 2 + bit;
		}
		in.insert(arr[i]);
	}

	vector< vector<int> > lot(1 << k);
	for (int i = 0; i < (1 << k); i++)
	{
		for (int j = 0; j < (1 << k); j++)
		{
			if ((i & j) == 0)
			{
				lot[i].push_back(j);
			}
		}
	}

	
	for (int i = 0; i < n; i++)
	{
		for (int j = 0; j < lot[arr[i]].size(); j++)
		{
			if (in.count(lot[arr[i]][j]))
			{
				cout << "YES" << endl;
				return;
			}
		}
	}

	cout << "NO" << endl;
}


int main()
{
#ifdef _DEBUG
	freopen("input.txt", "r", stdin);
	//freopen("output.txt", "w", stdout);
#endif
	srand(time(NULL));

	solve();

	return 0;
}