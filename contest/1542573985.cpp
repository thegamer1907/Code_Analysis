#include<bits/stdc++.h>
using namespace std;
int a1[2];
int a2[2][2];
int a3[2][2][2];
int a4[2][2][2][2];
int main()
{
    int n,m;
    int flag=0;
    int w,x,y,z;
    cin >> n >> m;

    if(m==1)
    {
        for(int i=0;i<n;i++)
        {
            cin >> w;
            a1[w]++;
            if(a1[0]!=0)
                flag=1;
        }
    }
    if(m==2)
    {
         for(int i=0;i<n;i++)
        {
            cin >> w >> x;
            a2[w][x]++;
            if(a2[0][0]!=0 || (a2[0][1]!=0 && a2[1][0]!=0) )
                flag=1;
        }
    }

    if(m==3)
    {
        for(int i=0;i<n;i++)
        {
            cin >> w >> x >> y;
            a3[w][x][y]++;
        }
        if(a3[0][0][0]!=0)
            flag=1;
        if(a3[0][0][1]!=0 && (a3[0][1][0]!=0 || a3[1][0][0]!=0 ||a3[1][1][0]!=0))
            flag=1;
        if(a3[0][1][0]!=0 && (a3[1][0][0]!=0 ||a3[1][0][1]!=0))
            flag=1;
        if(a3[0][1][1]!=0 && a3[1][0][0]!=0)
            flag=1;
    }
     if(m==4)
    {
         for(int i=0;i<n;i++)
        {
           cin >> w >> x >> y >> z;;
            a4[w][x][y][z]++;
        }
        if(a4[0][0][0][0]!=0)
            flag=1;
        if(a4[0][0][0][1]!=0 && (a4[0][0][1][0]!=0 || a4[0][1][0][0]!=0 || a4[0][1][1][0]!=0 || a4[1][0][0][0]!=0 || a4[1][0][1][0]!=0 || a4[1][1][0][0]!=0 || a4[1][1][1][0]!=0))
            flag=1;
        if(a4[0][0][1][0]!=0 && (a4[0][1][0][0]!=0 || a4[0][1][0][1]!=0 || a4[1][0][0][0]!=0 || a4[1][0][0][1]!=0 || a4[1][1][0][0]!=0 || a4[1][1][0][1]!=0))
            flag=1;
        if(a4[0][0][1][1]!=0 && (a4[0][1][0][0]!=0 || a4[1][0][0][0]!=0 || a4[1][1][0][0]!=0))
            flag=1;
        if(a4[0][1][0][0]!=0 && (a4[1][0][0][0]!=0 || a4[1][0][0][1]!=0 || a4[1][0][1][0]!=0 || a4[1][0][1][1]!=0))
            flag=1;
        if(a4[0][1][0][1]!=0 && (a4[1][0][0][0]!=0 || a4[1][0][1][0]!=0))
            flag=1;
        if(a4[0][1][1][0]!=0 && (a4[1][0][0][0]!=0 || a4[1][0][0][1]!=0))
            flag=1;
        if(a4[0][1][1][1]!=0 && a4[1][0][0][0]!=0 )
            flag=1;
    }
    if(flag==1)
        cout << "YES" << endl;
    else
        cout << "NO" << endl;
    return 0;
}
