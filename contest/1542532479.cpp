using namespace std;

int main() {
    int numwords;
    char password[3];
    char words[100][3];
    bool letters[2];
    letters[0] = false;
    letters[1] = false;
    scanf("%s%d" , password , &numwords);
    for (int a = 0; a < numwords; ++a){
        scanf("%s" , words[a]);
        if (words[a][0] == password[0] && words[a][1] == password[1]){
            printf("YES");
            return 0;
        }
        else {
            if (words[a][0] == password[1]) {
                letters[1] = true;
            }
            if (words[a][1] == password[0]) {
                letters[0] = true;
            }
        }
    }
    if (letters[1] == letters[0] && letters[0]){
        printf("YES");
    }
    else {
        printf("NO");
    }
    return 0;
}