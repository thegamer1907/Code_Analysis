void boost()
{
	std::ios_base::sync_with_stdio(false);
	cin.tie(NULL);
	cout.tie(NULL);
}

int solve(string s,int k,int n,int mid)
{
	int f[2]={0};
	for(int i=0;i<mid;i++)
	f[s[i]-'a']++;
	if(min(f[0],f[1])<=k)
	return 1;

	for(int i=mid;i<n;i++)
	{
		f[s[i-mid]-'a']--;
		f[s[i]-'a']++;
		if(min(f[0],f[1])<=k)
		return 1;
	}

	return 0;
}

int main()
{
	boost();
	int n,k;
	cin>>n>>k;
	string s;
	cin>>s;
	int lo=1,hi=n,ans=-1;
	while(lo<=hi)
	{
		int mid=(lo+hi)/2;
		if(solve(s,k,n,mid))
		{
			ans=mid;
			lo=mid+1;
		}
		else
		hi=mid-1;
	}

	cout<<ans<<endl;
	return 0;
}