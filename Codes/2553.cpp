#include <iostream>
#include <cstring>

using namespace std;

#define ll long long
#define N (ll)1e7

ll freq[N + 1], n, ans[N + 1];
bool is_prime[N + 1];
ll presum[N + 1];

void seive()
{
	for (ll i = 2; i <= N; i++)
		is_prime[i] = 1;

	for (ll i = 2; i*i <= N; i++)
		if (is_prime[i])
			for (ll j = i*i; j <= N; j += i)
				is_prime[j] = 0;
}

int main()
{
	ios::sync_with_stdio(false);

	seive();

	memset(freq, 0, sizeof(freq));

	cin>>n;
	for (ll i = 0; i < n; i++)
	{
		ll temp;
		cin>>temp;
		freq[temp]++;
	}

	for (ll p = 2; p <= N; p++)
		if (is_prime[p])
		{
			for (ll i = p; i <= N; i += p)
				ans[p] += freq[i];
		}

	presum[0] = 0;
	for (ll i = 1; i <= N; i++)
		presum[i] = presum[i - 1] + ans[i];

	ll q;
	cin>>q;
	while (q--)
	{
		ll l, r;
		cin>>l>>r;
		r = min((ll)1e7, r);
		l = min((ll)1e7, l);
		cout<<presum[r] - presum[l - 1]<<endl;
	}
	return 0;
}